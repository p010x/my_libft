#include "libft.h"

char        *ft_strchr(const char *s, int c)
{
    int i;

    i = 0;
    while (s[i])
    {
        if (s[i] == c)
            return (&s[i]);
        i++;
    }
    return ((s[i] == c) ? &s[i] : NULL);
}
